// ─── Helper functions ──────────────────────────────────────────────────────────

export function getDealBySlug(slug: string): Deal | undefined {
  return deals.find((d) => d.slug === slug)
}

// Platforms with active affiliate relationships — shown on public pages
const ACTIVE_PLATFORMS = new Set(["Sephora PH", "Temu"])

// All active deals sorted by discount % descending — only products with images
export function getActiveDeals(): Deal[] {
  return deals
    .filter(d => ACTIVE_PLATFORMS.has(d.platform) && !!d.imageUrl)
    .sort((a, b) => b.discount - a.discount)
}

// Top N active deals for homepage featured section
export function getFeaturedDeals(count = 6): Deal[] {
  return getActiveDeals().slice(0, count)
}

// All category labels present in active deals (for filter tabs)
export function getActiveCategories(): string[] {
  const cats = new Set(getActiveDeals().map(d => d.category))
  return ["All", ...Array.from(cats).sort()]
}

// Maps category page slugs to deal category values in this data file
const CATEGORY_SLUG_MAP: Record<string, string[]> = {
  "tech-deals":    ["Electronics"],
  "home-finds":    ["Home", "Outdoor"],
  "beauty":        ["Beauty", "Skincare"],
  "fashion":       ["Fashion"],
  "travel":        ["Travel"],
  "digital-tools": ["Digital Tools"],
}

export function getDealsByCategory(categorySlug: string): Deal[] {
  const active = getActiveDeals()
  if (categorySlug === "under-500") {
    return active.filter(d => d.salePrice < 500)
  }
  if (categorySlug === "under-1000") {
    return active.filter(d => d.salePrice < 1000)
  }
  if (categorySlug === "gift-ideas") {
    return active.filter(d => d.sulitScore >= 8)
  }
  const mapped = CATEGORY_SLUG_MAP[categorySlug]
  if (mapped) {
    return active.filter(d => mapped.includes(d.category))
  }
  return active.filter(d => d.category.toLowerCase() === categorySlug.toLowerCase())
}

export function getDealsByPlatform(platform: string): Deal[] {
  return getActiveDeals().filter(d => d.platform === platform)
}
